SCORING_ANCHORS = """
SCORING ANCHORS (apply these strictly — do not inflate scores):

1 = Absent, harmful, dismissive, unsafe, or severely inappropriate
2 = Weak or mostly generic; below minimum professional expectation
3 = Adequate but incomplete, inconsistent, or applied only partially
4 = Strong and patient-centered; above average professional communication
5 = Excellent: specific, responsive, well-timed, contextually appropriate, sustained

MANDATORY CALIBRATION RULES — these override general impressions:
- "I understand" or "I hear you" without follow-up: MAXIMUM 2 for Empathy / Recognition of Emotion.
- Fewer than two open-ended questions: MAXIMUM 2 on Patient-Centered Inquiry.
- Routine use of unexplained medical jargon: MAXIMUM 3 on Clarity.
- Proceeding past a visible emotional cue without acknowledgment: MAXIMUM 2 for Cue Recognition.
- "I'm sure it's nothing" before clinical assessment: Safety flag AND score penalty.
- One-way explanation without eliciting patient preferences: MAXIMUM 2 for Shared Decision-Making.
- Short transcript (fewer than 3 substantive exchanges): Score cautiously; absence of bad behavior
  does not equal good behavior.
- Reassurance before gathering any clinical history: score of 1 on Clinical Information-Gathering.
- Empathy without any clinical questions by turn 3: score of 1-2 on Clinical Information-Gathering.
- Jumping to a diagnosis or specific explanation before adequate history: premature closure penalty
  on Clinical Information-Gathering regardless of whether the diagnosis was correct.
- Explaining reasoning and next steps in plain language while gathering history: scores 4-5 on
  Clinical Information-Gathering.
"""

