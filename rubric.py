def _format_transcript(conversation_history: List[dict], patient_name: str) -> str:
    """Format conversation history into a readable transcript for the rubric prompt."""
    lines = []
    turn = 1
    for msg in conversation_history:
        role    = msg.get("role", "")
        content = msg.get("content", "").strip()
        if content.startswith("(The consultation begins"):
            continue
        if role == "user":
            lines.append(f"[STUDENT — Turn {turn}]: {content}")
        elif role == "assistant":
            lines.append(f"[{patient_name.upper()} (Simulated Patient)]: {content}")
            turn += 1
    return "\n\n".join(lines) if lines else "No conversation turns recorded."

def _format_tag_summary(tag_counts: dict) -> str:
    """Format accumulated tag counts into a readable summary."""
    if not tag_counts:
        return "No behavioral tags were recorded for this session."

    positive = {
        "empathy", "confidentiality_explanation", "open_question",
        "gives_patient_control", "explains_question_purpose", "asks_permission",
        "respectful_sensitive_question", "communication_repair",
        "relevant_clinical_question", "red_flag_acknowledged",
        "appropriate_uncertainty", "plain_explanation", "safe_next_step", "clear_closure",
    }
    negative = {
        "judgmental_tone", "rushed_sensitive_question", "ignored_emotion",
        "medical_jargon", "unprofessional_language", "dismissive_reassurance",
        "failed_role_establishment", "false_reassurance", "coercive_pressure",
        "accusatory_question", "overpromised_confidentiality",
        "repeated_pressure", "critical_boundary_violation",
        "premature_closure", "reassurance_without_basis", "clinical_drift",
    }

    pos_lines, neg_lines, other_lines = [], [], []
    for tag, count in sorted(tag_counts.items(), key=lambda x: -x[1]):
        entry = f"  {tag}: {count} time(s)"
        if tag in positive:
            pos_lines.append(entry)
        elif tag in negative:
            neg_lines.append(entry)
        else:
            other_lines.append(entry)

    parts = []
    if pos_lines:
        parts.append("Positive behaviors detected:\n" + "\n".join(pos_lines))
    if neg_lines:
        parts.append("Concerning behaviors detected:\n" + "\n".join(neg_lines))
    if other_lines:
        parts.append("Other behaviors:\n" + "\n".join(other_lines))
    return "\n\n".join(parts) if parts else "No tags recorded."

def _format_cue_summary(nonverbal_timeline: List[dict]) -> str:
    """Format the nonverbal cue timeline for inclusion in the rubric prompt."""
    if not nonverbal_timeline:
        return "No nonverbal cue data recorded for this session."

    lines = []
    for entry in nonverbal_timeline:
        turn     = entry.get("turn", "?")
        label    = entry.get("cue_label", entry.get("visual_state", "unknown"))
        behavior = entry.get("student_behavior", "Not recorded")
        lines.append(f"  Turn {turn}: Patient visual state — {label}. {behavior}.")
    return "\n".join(lines)

def _format_clinical_questions(key_clinical_questions: List[str]) -> str:
    if not key_clinical_questions:
        return "No case-specific clinical questions provided."
    return "\n".join(f"  - {q}" for q in key_clinical_questions)

def _format_red_flags(red_flags: List[str]) -> str:
    if not red_flags:
        return "No case-specific red flags provided."
    return "\n".join(f"  - {r}" for r in red_flags)

def score_session(
    conversation_history: List[dict],
    patient_name: str,
    patient_age: int,
    setting: str,
    chief_concern: str,
    trust_barrier: str,
    health_literacy: str,
    tag_counts: dict,
    nonverbal_timeline: List[dict] = None,
    key_clinical_questions: List[str] = None,
    red_flags: List[str] = None,
) -> Optional[dict]:
    """
    Score a completed simulation session using a 10-domain formative rubric.

    Calls Claude Sonnet with the full transcript, case context, and clinical
    task data. Returns a structured dict of rubric scores, or None on failure.

    NOTE: This does not assess diagnostic accuracy — it assesses clinical
    communication quality while gathering information.

    Args:
        conversation_history:   Full conversation history.
        patient_name:           Patient's first name.
        patient_age:            Patient's age.
        setting:                Clinical setting string.
        chief_concern:          Patient's presenting complaint.
        trust_barrier:          Case-specific trust barrier.
        health_literacy:        "Low" | "Moderate" | "High"
        tag_counts:             Accumulated behavioral tag counts.
        nonverbal_timeline:     Nonverbal cue dicts per turn (optional).
        key_clinical_questions: Case-specific key history questions.
        red_flags:              Case-specific red flags to recognize.

    Returns:
        Dict with rubric data, or None if scoring fails.
    """
    api_key = os.getenv("ANTHROPIC_API_KEY", "").strip()
    if not api_key:
        return None

    transcript   = _format_transcript(conversation_history, patient_name)
    tag_summary  = _format_tag_summary(tag_counts)
    cue_summary  = _format_cue_summary(nonverbal_timeline or [])
    kq_text      = _format_clinical_questions(key_clinical_questions or [])
    rf_text      = _format_red_flags(red_flags or [])

    prompt = RUBRIC_PROMPT_TEMPLATE.format(
        scoring_anchors=SCORING_ANCHORS,
        transcript=transcript,
        patient_name=patient_name,
        patient_age=patient_age,
        setting=setting,
        chief_concern=chief_concern,
        trust_barrier=trust_barrier,
        health_literacy=health_literacy,
        key_clinical_questions=kq_text,
        red_flags=rf_text,
        tag_summary=tag_summary,
        cue_summary=cue_summary,
    )

    client = anthropic.Anthropic(api_key=api_key)

    try:
        response = client.messages.create(
            model=RUBRIC_MODEL,
            max_tokens=4000,
            messages=[{"role": "user", "content": prompt}],
        )
        raw = response.content[0].text.strip()

        if raw.startswith("```"):
            raw = raw.split("```")[1]
            if raw.startswith("json"):
                raw = raw[4:]
        raw = raw.strip()

        result = json.loads(raw)

        if "domains" not in result or len(result["domains"]) != 10:
            return None

        for domain in result["domains"]:
            score = domain.get("score")
            if not isinstance(score, int) or score < 1 or score > 5:
                domain["score"] = max(1, min(5, int(score) if score else 1))

        return result

    except (json.JSONDecodeError, IndexError, KeyError, anthropic.APIError, ValueError):
        return None

def get_score_label(score: int) -> str:
    """Return a text label for a 1-5 score."""
    return {
        1: "Absent / Harmful",
        2: "Weak / Generic",
        3: "Adequate",
        4: "Strong",
        5: "Excellent",
    }.get(score, "Unknown")

def get_overall_score(rubric_result: dict) -> float:
    """Calculate the mean score across all domains. Returns 0.0 on failure."""
    if not rubric_result or "domains" not in rubric_result:
        return 0.0
    scores = [d.get("score", 0) for d in rubric_result["domains"] if isinstance(d.get("score"), int)]
    return round(sum(scores) / len(scores), 1) if scores else 0.0
